const mongoose = require("mongoose");

// create product schema
const usrSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
    versionKey: false,
  }
);

// export schema model
module.exports.UsrModel = mongoose.model("User", usrSchema);
